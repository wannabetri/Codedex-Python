def dog_years(name, age):
    # Calculate the dog's age in human years using the 7:1 ratio
    human_age = age * 7

    # Create a formatted string
    result = f"{name} is {human_age} years old in human years."

    return result


# Example usage:
dog_years("Landon", 3)  # Output: "Landon is 21 years old in human years."
dog_years("Red Bean", 6)  # Output: "Red Bean is 42 years old in human years."
dog_years("Cooper", 2)  # Output: "Cooper is 14 years old in human years."
